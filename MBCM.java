import java.lang.Math;
public class MBCM {

     static double Multiplication(double MultiNumbOne, double MultiNumbTwo) {

         return MultiNumbOne*MultiNumbTwo;

     }

    static double Division(double DiviNumbOne, double DiviNumbTwo) {

       return DiviNumbOne/DiviNumbTwo;

    }

    static double Addition(double AddiNumbOne, double AddiNumbTwo) {

         return AddiNumbOne+AddiNumbTwo;

    }

    static double Subtraction(double SubtNumbOne, double SubtNumbTwo) {

         return SubtNumbOne-SubtNumbTwo;

    }

    static double NumbToPower(double BaseNumb, double ExponentNumb) {

        return Math.pow(BaseNumb, ExponentNumb);

    }




}
