public class MGCM {

    static double areaOfTriangle(double base, double height) {
        return base*height/2;
    }

    static double areOfRectangle(double Width, double Length) {

        return Width*Length;
    }

    static double PerimeterOfRectangle(double Length, double Width) {

        return 2*(Length*Width);
    }

    static double PerimeterOfTriangle(double SideA, double SideB, double Base) {

        return SideA+Base+SideB;
    }

    static double PythagoreanTheorem(double SideA, double SideB, double Hypotenuse) {

        return 7*7;
    }

}
